
public class Groente {

    private long id;
    private String naam;
    private String wetenschappelijkeNaam;
    private int masHoogte;
    private int minHoogte;
    private int duurTotPluk;
    private String oogsttijd;
    private String beschrijving;
    private int plantafstand;
    private String grondsoort;
    private String licht;
    private String plaats;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNaam() {
        return naam;
    }

    public void setNaam(String naam) {
        this.naam = naam;
    }

    public String getWetenschappelijkeNaam() {
        return wetenschappelijkeNaam;
    }

    public void setWetenschappelijkeNaam(String wetenschappelijkeNaam) {
        this.wetenschappelijkeNaam = wetenschappelijkeNaam;
    }

    public int getMasHoogte() {
        return masHoogte;
    }

    public void setMasHoogte(int masHoogte) {
        this.masHoogte = masHoogte;
    }

    public int getMinHoogte() {
        return minHoogte;
    }

    public void setMinHoogte(int minHoogte) {
        this.minHoogte = minHoogte;
    }

    public int getDuurTotPluk() {
        return duurTotPluk;
    }

    public void setDuurTotPluk(int duurTotPluk) {
        this.duurTotPluk = duurTotPluk;
    }

    public String getOogsttijd() {
        return oogsttijd;
    }

    public void setOogsttijd(String oogsttijd) {
        this.oogsttijd = oogsttijd;
    }

    public String getBeschrijving() {
        return beschrijving;
    }

    public void setBeschrijving(String beschrijving) {
        this.beschrijving = beschrijving;
    }

    public int getPlantafstand() {
        return plantafstand;
    }

    public void setPlantafstand(int plantafstand) {
        this.plantafstand = plantafstand;
    }

    public String getGrondsoort() {
        return grondsoort;
    }

    public void setGrondsoort(String grondsoort) {
        this.grondsoort = grondsoort;
    }

    public String getLicht() {
        return licht;
    }

    public void setLicht(String licht) {
        this.licht = licht;
    }

    public String getPlaats() {
        return plaats;
    }

    public void setPlaats(String plaats) {
        this.plaats = plaats;
    }
}
